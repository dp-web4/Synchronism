import json, os
import numpy as np


def K(k, a):                       # lattice spatial dispersion factor
    return (2.0/a)*np.sin(k*a/2.0)


def self_energy(p0, p, m, g, a, ds=1, Nw=2400, Nk=1200, Wmax=12.0):
    """1-loop φ³ bubble Σ(p0,p) by direct quadrature.
       ω continuous on [-Wmax,Wmax]; k on the Brillouin zone.
       ds = effective number of SPATIAL dimensions: the radial phase-space weight
       |k|^(ds−1) is inserted to tune the loop's UV sensitivity (ds=1: UV-finite /
       super-renormalizable; ds=3: D=4 spacetime, UV-log-divergent / marginal). This is a
       power-counting proxy for going to 4D, not a full 4D lattice loop."""
    w = np.linspace(-Wmax, Wmax, Nw)          # continuous time-frequency
    dw = w[1]-w[0]
    k = np.linspace(-np.pi/a, np.pi/a, Nk, endpoint=False)
    dk = k[1]-k[0]
    W, Kk = np.meshgrid(w, k, indexing='ij')  # (Nw,Nk)
    wt = np.abs(Kk)**(ds-1)                    # radial spatial phase-space weight
    G1 = 1.0/(W**2 + K(Kk, a)**2 + m**2)
    # second propagator at shifted external momentum (K is 2π/a-periodic ⇒ no explicit wrap)
    G2 = 1.0/((p0-W)**2 + K(p - Kk, a)**2 + m**2)
    integ = wt*G1*G2
    return (g*g/2.0) * integ.sum() * dw * dk / (2*np.pi)**2


def quad_coeff(xs, ys):
    """fit y = c0 + c2 x^2 (even), return c2."""
    A = np.vstack([np.ones_like(xs), xs**2]).T
    coef, *_ = np.linalg.lstsq(A, ys, rcond=None)
    return coef[1]


def c_liv_for(m, g, a, ds=1):
    ps = np.array([0.0, 0.10, 0.20, 0.30])
    S_t = np.array([self_energy(pp, 0.0, m, g, a, ds) for pp in ps])   # vary time-momentum
    S_s = np.array([self_energy(0.0, pp, m, g, a, ds) for pp in ps])   # vary space-momentum
    A = quad_coeff(ps, S_t)     # coeff of p0^2
    B = quad_coeff(ps, S_s)     # coeff of p^2
    return A, B, (B/A - 1.0)


def decoupling_power(ds, a, ms=(0.50,0.25,0.125,0.0625)):
    cl=[]
    for m in ms:
        _,_,c=c_liv_for(m, m, a, ds)   # g=m (fixed ĝ)
        cl.append(abs(c))
    ma=np.array(ms); cl=np.array(cl)
    return float(np.polyfit(np.log(ma),np.log(cl),1)[0]), list(zip(ms,[float(x) for x in cl]))


def c_liv_vs_kcut(m, g, a, Nw=2400, Nk=1200, Wmax=12.0):
    """Accumulate the LIV piece (A-B) as a function of an internal-|k| cutoff, to see
       whether c_LIV is sourced in the IR (small k) or the UV (k~π/a)."""
    w = np.linspace(-Wmax, Wmax, Nw); dw=w[1]-w[0]
    k = np.linspace(-np.pi/a, np.pi/a, Nk, endpoint=False); dk=k[1]-k[0]
    W, Kk = np.meshgrid(w, k, indexing='ij')
    dp = 0.2
    def integrand(p0, p):
        G1 = 1.0/(W**2 + K(Kk,a)**2 + m**2)
        G2 = 1.0/((p0-W)**2 + K(p-Kk,a)**2 + m**2)
        return (g*g/2.0)*G1*G2/(2*np.pi)**2
    # second-difference kernels in p0 and p (curvature integrands)
    cur_t = (integrand(dp,0)-2*integrand(0,0)+integrand(-dp,0))/dp**2     # ~A density
    cur_s = (integrand(0,dp)-2*integrand(0,0)+integrand(0,-dp))/dp**2     # ~B density
    liv_density = (cur_t - cur_s)            # (A-B) density over (ω,k)
    # integrate over ω, leaving a function of |k|; cumulative over |k|
    dens_k = liv_density.sum(axis=0)*dw      # per-k
    kk = np.abs(k)
    order = np.argsort(kk)
    kk_s = kk[order]; dens_s = dens_k[order]*dk
    cum = np.cumsum(dens_s)
    total = cum[-1]
    # fraction of |A-B| accumulated below |k| thresholds
    fracs={}
    for frac_k in [0.25,0.5,0.75,0.9]:
        kthr = frac_k*np.pi/a
        fracs[frac_k] = float(cum[np.searchsorted(kk_s,kthr)-1]/total) if total!=0 else float('nan')
    return fracs, float(total)


